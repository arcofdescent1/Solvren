/**
 * Phase 7 — Scan migrations + schema for public tables that reference organizations
 * and emit org column (org_id vs organization_id). Writes:
 * - docs/architecture/org_purge_inventory.md (overview)
 * - src/modules/org-purge/org-purge-tables.generated.ts (planner/verifier input)
 *
 * Usage: node scripts/generate-org-purge-inventory.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const migrationsDir = path.join(root, "supabase", "migrations");
const schemaPath = path.join(root, "supabase", "schema.sql");

/** @type {Map<string, 'org_id' | 'organization_id'>} */
const tables = new Map();

function ingestCreateBlocks(sql, publicPrefixOnly) {
  const re = publicPrefixOnly
    ? /create table if not exists public\.(\w+)/gi
    : /create table if not exists (\w+)/gi;
  let m;
  const starts = [];
  while ((m = re.exec(sql)) !== null) {
    starts.push({ name: m[1].toLowerCase(), idx: m.index });
  }
  for (let i = 0; i < starts.length; i++) {
    const { name, idx } = starts[i];
    const end = i + 1 < starts.length ? starts[i + 1].idx : sql.length;
    const block = sql.slice(idx, end).slice(0, 5000).toLowerCase();
    if (!block.includes("organizations")) continue;
    let col = null;
    if (/organization_id uuid/.test(block)) col = "organization_id";
    else if (/org_id uuid/.test(block)) col = "org_id";
    if (!col) continue;
    tables.set(name, col);
  }
}

for (const f of fs.readdirSync(migrationsDir).filter((x) => x.endsWith(".sql"))) {
  ingestCreateBlocks(fs.readFileSync(path.join(migrationsDir, f), "utf8"), true);
}
ingestCreateBlocks(fs.readFileSync(schemaPath, "utf8"), false);

const extra = [
  ["change_events", "org_id"],
  ["approvals", "org_id"],
  ["notification_outbox", "org_id"],
  ["in_app_notifications", "org_id"],
  ["organization_members", "org_id"],
  // Phase 3 — intake / custom sources (ensure listed if migration not yet in schema.sql snapshot)
  ["import_batches", "org_id"],
  ["custom_sources", "org_id"],
];
for (const [t, c] of extra) tables.set(t, /** @type {'org_id'} */ (c));

/** Tables with no org_id: purged via FK CASCADE when parent org-scoped rows are removed. */
const cascadeNoteLines = [
  "`custom_source_external_records` — CASCADE from `custom_sources` (which is org-scoped).",
];

const arr = [...tables.entries()].sort((a, b) => a[0].localeCompare(b[0]));

const ts =
  `/**\n * AUTO-GENERATED by scripts/generate-org-purge-inventory.mjs — do not edit by hand.\n */\n` +
  `export const ORG_PURGE_TABLE_SPECS = [\n` +
  arr.map(([t, c]) => `  { table: "${t}", orgColumn: "${c}" as const },`).join("\n") +
  `\n] as const;\n`;

const outTs = path.join(root, "src", "modules", "org-purge", "org-purge-tables.generated.ts");
fs.mkdirSync(path.dirname(outTs), { recursive: true });
fs.writeFileSync(outTs, ts);

const md = `# Org purge inventory (generated)

See also: [Retention exceptions](./org_purge_retention_exceptions.md).

This file is regenerated by \`node scripts/generate-org-purge-inventory.mjs\` (or \`npm run generate:org-purge-inventory\`).

## Summary

- Tables scanned: **${arr.length}**
- Column: \`org_id\` or \`organization_id\` (see generated TS).

## Classification (v1 heuristic)

| Pattern | Rule |
|--------|------|
| FK to \`organizations(id) ON DELETE CASCADE\` | **PURGE_BY_CASCADE** — removed when org row is deleted |
| \`audit_log\` | **PURGE_EXPLICIT** — no FK to organizations; delete before org delete |
| \`billing_accounts\` | **RETAIN_EXCEPTION** — finance snapshot in \`org_purge_finance_retention_snapshots\` then row cascades away |
| \`policies.org_id IS NULL\` | **PLATFORM_SHARED** — not selected by \`eq('org_id', orgId)\` |
| \`org_purge_*\` | **AUDIT_ARTIFACT** — not counted toward “zero org rows” |

## Table list

${arr.map(([t, c]) => `- \`${t}\` — \`${c}\``).join("\n")}

## CASCADE-only (no org_id column)

${cascadeNoteLines.map((line) => `- ${line}`).join("\n")}
`;

const outMd = path.join(root, "docs", "architecture", "org_purge_inventory.md");
fs.mkdirSync(path.dirname(outMd), { recursive: true });
fs.writeFileSync(outMd, md);

console.info(`Wrote ${arr.length} tables → ${path.relative(root, outTs)} and ${path.relative(root, outMd)}`);
